public class Block {
    // Block {}
    public static void main(String[] args) {
        // Expression hello_world = "Hello World"
        var hello_world = "Hello World";

        //Statement System.out.println(hello_world);
        System.out.println(hello_world);

        // Block {}
        {
            System.out.println(hello_world);
        }
    }
}
